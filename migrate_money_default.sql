-- money 컬럼의 DEFAULT 값을 200000으로 변경
-- 기존 데이터는 유지되고, 새로 생성되는 행만 200000원을 기본값으로 가집니다.

-- SQLite는 ALTER TABLE로 DEFAULT 값을 직접 변경할 수 없으므로
-- 새 컬럼을 만들고 데이터를 복사한 후 기존 컬럼을 삭제하는 방식이 필요합니다.
-- 하지만 더 간단한 방법은 기존 유저들의 money를 업데이트하는 것입니다.

-- 기존 유저들의 money를 200000으로 업데이트 (선택사항)
-- UPDATE users SET money = 200000 WHERE money < 200000;

-- 또는 DEFAULT 값만 변경하려면 테이블을 재생성해야 합니다.
-- 이 경우 기존 데이터를 백업하고 복원해야 합니다.
